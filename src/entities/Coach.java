package entities;

/**
 *
 * @author Asus
 */
public class Coach {
    private int id;

    public Coach(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Coach{" + "id=" + id + '}';
    }
    
}
